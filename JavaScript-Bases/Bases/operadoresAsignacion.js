// ! Operadores de asignacion
// * Operador = para asignar un valor

let miNumero = 10;
console.log(miNumero);


miNumero = 20;
console.log(miNumero);

// ! Operador compuesto +=

miNumero += 5 // ? miNumero = miNumero + 5
console.log(miNumero);

// ! Operador compuesto -=

miNumero -= 3
console.log(miNumero);

// ! Operador compues *=

miNumero *= 2;
console.log(miNumero);

// ! Operador compuesto /=

miNumero /= 4;
console.log(miNumero);

// ! Operador %=
miNumero %= 2;
console.log(miNumero);

// ! Operador exponente
miNumero = 2 ** 3
console.log(miNumero); // ? 2 * 2 * 2 = 8

// ! Operador compuesto **=
miNumero **=2;
console.log(miNumero);