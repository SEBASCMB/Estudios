// ! Operadores relaciones (comparaciones)

let a = 10;
let b = 5;
console.log(a);
console.log(b);


// * Operador igualdad == (Solo compara valores)

console.log("a == b ->", a == b);
console.log(`${a} == ${b} ->  ${a == b}`);

// * Operador igualda estricta y exacta (compara el valor y tipo de dato) ===
console.log(`${a} == ${b} ->  ${a === b}`);

// * Operador distinto (solo compara valor)

console.log(`${a} != ${b} -> ${a != b}`);

// * Operador distinto exacto (comÃ ra valor y tipo)

console.log(`${a} !== ${b} -> ${a !== b}`);


// * Operador mayor que

console.log(`${a} > ${b} ->  ${a > b}`);

// * Operador menor que

console.log(`${a} < ${b} ->  ${a < b}`);

// * Operador mayor o igual que

console.log(`${a} >= ${b} ->  ${a >= b}`);

// * Operador menor o igual que

console.log(`${a} <= ${b} ->  ${a <= b}`);

