export function SubstractTwoNumbers(num1:number, num2: number = 0): number {
  return num1 - num2
}

export function timesTwoNumbers(num1:number, num2: number = 0): number {
  return num1 * num2
}

export function divideTwoNumbers(num1:number, num2: number = 0): number {
  return num1 / num2
}